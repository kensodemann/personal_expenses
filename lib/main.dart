    return Scaffold(
      appBar: AppBar(
        title: Text(title),
      ),
      body: Column(
        crossAxisAlignment: CrossAxisAlignment.stretch,
        children: const [
          Card(
            child: Text('Chart'),
          ),
          Card(
            child: Text('List of Transactions'),
          ),
        ],
